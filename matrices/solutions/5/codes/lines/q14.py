import numpy as np
A = np.array([[3,-1,-2],[0,0,-1],[3,-5,0]])
B = np.array([[3,-4,5],[1,1,-2],[2,3,1]])
C = np.array([[0,1,2],[-1,0,-3],[-2,3,0]])
D = np.array([[2,-1,-2],[0,2,-1],[3,-5,0]])

print("The determinant value of A is",np.linalg.det(A))
print("The determinant value of B is",np.linalg.det(B))
print("The determinant value of C is",np.linalg.det(C))
print("The determinant value of D is",np.linalg.det(D))
