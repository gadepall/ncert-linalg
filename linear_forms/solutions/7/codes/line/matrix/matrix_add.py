import numpy as np
import matplotlib.pyplot as plt
import math

#Matrices
A = np.array([[math.sqrt(3),1,-1],[2,3,0]]) 
B = np.array([[2,math.sqrt(5),1],[-2,3,1/2]])

#Checking if both matrices have similar number of rows and columns
row_A=len(A)
row_B=len(B)
col_A=len(A[0])
col_B=len(B[0])
print(row_A,row_B,col_A,col_B)
#let C be the sum of two matrices

if row_A==row_B and col_A==col_B:
	C= np.add(A,B)
	print(C) 
else:
	print("Matrix addition not possible")
